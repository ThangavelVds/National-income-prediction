a=read.csv(file="F:/College/Study_sem_4/Project/R/Dataset/ds_2.csv")
print("The GDP AND GNP DATAS OF INDIA FROM 1962 TO 2018")
print(a)
x=c(a$year)
y=c(a$y)
gdp=c(a$gdp)
gnp=c(a$gnp)
xy=c(a$xy)
print("National income from 1962 to 2018")
print(xy)
model<-lm(x~y)
sx=sum(a$year)
sy=sum(a$y)
sxy=sum(xy)
ssx=sum(a$sx)
ssy=sum(a$sy)
a=((ssx*sy)-(sx*sxy))/((57*(ssx))-(sx^2))
b=((57*sxy)-(sx*sy))/((57*(ssx))-(sx^2))
print("General equation")
print("Y=a+bX")
print("X-Time(Years)")
print("Y-National Income")
print("a value")
print(a)
print("b value")
print(b)
par(mfrow = c(1,3))
plot(x,gdp,main="Year Vs GDP(in $B)",xlab = "Year",ylab ="GDP",col="red",type="o")
plot(x,gnp,main="Year Vs GNP(in $B)",xlab = "Year",ylab ="GNP",col="blue",type="o")
plot(x,y,main="Year Vs National Income(in $B)",xlab = "Year",ylab="National Income",col="green",type="o")
pvar=as.integer(readline(prompt = "National income prediction for the year :"))
print("Predicted National Income for the given year")
summary(model)
print(a+(b*pvar))
plot(model)